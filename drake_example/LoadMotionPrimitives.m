    elseif(orient == 1)
        rotmat = [0.,-1., 0; 1.,0. 0; 0, 0, 1];
    elseif(orient == 2)
        rotmat = [-1.,0., 0; 0.,-1. 0; 0, 0, 1];
    elseif((orient == 3) || (orient == -1))
        rotmat = [0.,1., 0; -1.,0. 0; 0, 0, 1];
    else
        disp('bad orientation parameter')
        rotmat = eye(3);
    end
end